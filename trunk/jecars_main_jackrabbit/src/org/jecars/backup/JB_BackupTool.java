    private void mJeCARS_checkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mJeCARS_checkActionPerformed
      try {
        URL u = new URL( mJeCARS_url.getText() );
        HttpURLConnection uc = (HttpURLConnection)u.openConnection();
        String userPassword = mJeCARS_username.getText() + ":" + new String(mJeCARS_pw.getPassword());
        String encoding = BASE64Encoder.encodeBuffer( userPassword.getBytes() );
        uc.setRequestProperty ("Authorization", "BASIC " + encoding);
        int code = uc.getResponseCode();
        if (code==HttpURLConnection.HTTP_OK) {            
          JOptionPane.showMessageDialog( this, "Connection is ok!" );
        } else {
          JOptionPane.showMessageDialog( this, "Error response code: " + code );
        }
      } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog( this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE );
      }
      return;
    }//GEN-LAST:event_mJeCARS_checkActionPerformed

    private void mSelectImExportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mSelectImExportActionPerformed
      JFileChooser fc = new JFileChooser();
      fc.setFileSelectionMode( JFileChooser.DIRECTORIES_ONLY  );
      if (fc.showOpenDialog( this )==JFileChooser.APPROVE_OPTION) {
        try {
          mExportDirectory.setText(fc.getSelectedFile().getCanonicalPath());
        } catch (IOException ex) {
          JOptionPane.showMessageDialog( this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE );
        }
      }
      return;
}//GEN-LAST:event_mSelectImExportActionPerformed

