    private void mJR_ExportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mJR_ExportActionPerformed
      try {
        JB_Session session = new JB_Session();
        session.setRepDirectory( mJR_RepDir.getText() );
        JB_ExportData export = new JB_ExportData();
        JB_Options options = new JB_Options();
        options.setExportDirectory( new File(mExportDirectory.getText()) );
        options.addExcludePath( "/jcr:system.*" );
        export.exportToDirectory( session.getSession().getRootNode(), options );
      } catch (Exception ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog( this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE );
      } finally {
      }
      return;
    }//GEN-LAST:event_mJR_ExportActionPerformed

    private void mJR_ImportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mJR_ImportActionPerformed
      try {
        JB_Session session = new JB_Session();
        session.setRepDirectory( mJR_RepDir.getText() );
        JB_ImportData imp = new JB_ImportData();
        JB_Options options = new JB_Options();
        options.setImportNamespaces( true );
        options.setImportDirectory( new File(mExportDirectory.getText()) );
        imp.importFromDirectory( session.getSession().getRootNode(), options );
      } catch (Exception ex) {
        JOptionPane.showMessageDialog( this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE );
      } finally {
      }
      return;
    }//GEN-LAST:event_mJR_ImportActionPerformed
    
